/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class BSTIterator {
public:
    BSTIterator(TreeNode* root) {
        head = root;
    }
    
    int next() {
        while (head)
        {
            st.push(head);
            head = head->left;
        }
        head = st.top();
        st.pop();
        int res = head->val;
        head = head->right;
        return res;
    }
    
    bool hasNext() {
        return head || !st.empty();
    }
private:
    TreeNode* head;
    stack<TreeNode*> st;
};

